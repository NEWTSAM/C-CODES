#include<stdio.h>

int main()
{
     int a,b;
     printf("Enter the values of a and b : \n");
     scanf("%d%d", &a,&b );
     printf("\n The < value of a is %d", a<b);
     printf("\n The <= value of a is %d", a<=b);
     printf("\n The > value of a is %d", a>b);
     printf("\n The >= value of a is %d", a>=b);
     printf("\n The == value of a is %d", a==b);
     printf("\n The != value of a is %d", a!=b);
     return 0;
     

}